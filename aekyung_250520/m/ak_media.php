    function youtubeAPI(listId,gubun){
        $.ajax({
            url: "/main/youtubeAPI.php",
            type: "post",
            data: {
                listId: listId,
            }
        }).success(function(response){
            response = response.replace('\n}\n\r\n0','\n}\n\r'); //마지막에 replace
            var result = JSON.parse(response);
            // result = JSON.parse(result).items;
            result = result.items; //fsockopen로 바꾸면서 수정

            var first_check = 1;

            var youtube_first = '';
            var youtube_list = '';

            $.each(result, function(i, item) {
                var thumbnails = item.snippet.thumbnails;
                var url =item.snippet.resourceId.videoId;

                // thumbnails가 정의되어 있고, 빈 객체가 아닌지 확인
                if (thumbnails && !$.isEmptyObject(thumbnails)) {
                    var title = item.snippet.title;
                    var videoId = item.snippet.resourceId.videoId;

                    if(first_check === 1){
                        var thumbnails_url = thumbnails.standard.url;

                        youtube_first += '<div onclick="window.open(\'https://www.youtube.com/watch?v='+url+'\')">';
                        youtube_first += '  <img src="'+thumbnails_url+'" alt="영상">';
                        youtube_first += '  <p class="video_tit fz14">'+title+'</p>';
                        youtube_first += '</div>';
                    }else {
                        var thumbnails_url = thumbnails.standard.url;

                        youtube_list += '<div onclick="window.open(\'https://www.youtube.com/watch?v='+url+'\')">';
                        youtube_list += '   <li><img src="'+thumbnails_url+'" alt="영상"></li>';
                        youtube_list += '</div>';
                    }
                    first_check++;
                }
            });
            $("."+gubun+"_first").html(youtube_first);
            $("."+gubun+"_list").html(youtube_list);
        })
    }
</script>
