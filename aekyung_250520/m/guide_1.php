<script>
    const body = document.querySelector('body');
    const html = document.querySelector('html');
    let scrollPosition = 0;

    // 팝업 아래 스크롤 움직임 제어하기
    function preventScroll(){
        // body, html smooth 옵션 제어
        body.classList.add("scroll_prevent");
        html.classList.add("scroll_prevent");
        scrollPosition = window.pageYOffset;
        body.style.overflow = 'hidden';
        body.style.position = 'fixed';
    }
    function defaultScroll(){
        body.style.removeProperty('overflow');
        body.style.removeProperty('position');
        window.scrollBy(0, scrollPosition);
        body.classList.remove("scroll_prevent");
        html.classList.remove("scroll_prevent");        
    }

    // AK Lover contents 팝업 활성화 / 닫기
    const popupBtn = document.querySelectorAll(".pop_btn");
    const popupContents = document.querySelectorAll(".popup");
    const closeBtn = document.querySelectorAll(".btn_x");

    popupBtn.forEach((item, i) => {
        item.addEventListener("click", () => {
            popupContents[i].style.display = "flex";
            preventScroll();
        })
    });

    for(let i = 0; i < closeBtn.length; i++){
        closeBtn[i].addEventListener("click", () => {
            const scrollContainer = popupContents[i].querySelector(".scroll");
            scrollContainer.scrollTo(0, 0); // 팝업 스크롤 위치 초기화
            popupContents[i].style.display = "none";
            defaultScroll();
        })
    }

    // 연혁 가상 요소 Height 제어하기
    let rootAfterStyle = document.createElement("style");
    const elements = document.querySelectorAll(".history_list > li");

    elements.forEach((item, idx) => {
        if(idx !== elements.length - 1){
            rootAfterStyle.innerHTML += `.history_list > li:nth-child(${idx + 1})::after {
                content: "";
                position: absolute;
                z-index : 3;
                height: ${item.offsetHeight}px;
                left: -2.3rem;
                top: 6rem;
                width: 1px;
                background-color: #e5e5e5;
            }`
        }
    });

    document.head.appendChild(rootAfterStyle);

</script>