<script>
    // 기존 에디터 스폰서 배너 클립보드 복사하기
    var clipboard = new Clipboard('.btn_clip');
    clipboard.on('success', function(e) {
        alert("복사 되었습니다. 블로그 등에 소스 붙여넣기 해주세요.");
        console.log(e);
    });

    clipboard.on('error', function(e) {
        console.log(e);
    });

    // 기본 위젯 클립보드 복사하기
    var clipboard2 = new Clipboard('.btn_clip2');
    clipboard2.on('success', function(e) {
        alert("복사 되었습니다. 블로그 등에 소스 붙여넣기 해주세요.");
        console.log(e);
    });

    clipboard2.on('error', function(e) {
        console.log(e);
    });

    // 문구 복사하기
    function copyClipBoard(id){
        const text = document.getElementById(id);
        let range = document.createRange();
        range.selectNode(text.childNodes[0]);

        window.getSelection().removeAllRanges(); // 기존 정보 제거
        window.getSelection().addRange(range); 

        document.execCommand("copy");  // 복사  
        window.getSelection().removeRange(range); // 선택 해제

        alert("공정위 문구가 복사되었습니다.");
    }

</script>
