  ④ 본조 제2항, 제3항의 회사조치에 대하여 회원은 회사가 정한 절차에 따라 이의신청을 할 수 있습니다.  <br />
  ⑤ 전항의 이의신청이 정당하다고 회사가 인정하는 경우, 회사는 즉시 서비스의 이용을 재개해야 합니다. 
