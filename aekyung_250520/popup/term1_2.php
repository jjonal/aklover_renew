<h2>제4장 서비스 이용</h2>
<p><strong>제 15조 (회원 ID와 비밀번호 관리에 대한 회원의 의무와 책임) </strong></p>
<p>① 아이디(ID)와 비밀번호에 관한 모든 관리책임은 회원에게 있습니다. 회원에게 부여된 아이디(ID)와 비밀번호의 관리소홀, 부정사용에 의하여 발생하는 모든 결과에 대한 책임은 회원에게 있습니다. <br />
  ② 자신의 아이디(ID)가 타인에 의해 무단 이용되는 경우 회원은 반드시 회사에 그 사실을 통보해야 합니다. <br />
  ③ 회원의 아이디(ID)는 회사의 사전 동의 없이 변경할 수 없습니다. <br />
</p>
<p><strong>제 16조 (정보의 제공) </strong></p>
<p>회사는 회원이 서비스 이용 도중 필요가 있다고 인정되는 다양한 정보에 대해서 전자우편이나 서신우편 등의 방법으로 회원에게 제공할 수 있습니다. </p>
<p><strong>제 17조 (회원의 게시물) </strong></p>
<p>① 게시물이라 함은 회원이 서비스를 이용하면서 게시한 글, 사진, 각종 파일과 링크 등을 말합니다.<br />
  ② 회원이 서비스에 등록하는 게시물 등으로 인하여 본인 또는 타인에게 손해나 기타 문제가 발생하는 경우 회원은 이에 대한 책임을 지게되며, 회사는 특별한 사정이 없는 한 이에 대하여 책임을 지지 않습니다.<br />
  ③ 회사는 다음 각 호에 해당하는 게시물 등을 회원의 사전 동의없이 임시게시 중단, 수정, 삭제,이동 또는 등록 거부 등의 관련 조치를 취할수 있습니다.<br />
 &ensp; 1. 다른 회원 또는 제 3자에게 심한 모욕을 주거나 명예를 손상시키는 내용인 경우  <br />
 &ensp; 2. 공공질서 및 미풍양속에 위반되는 내용을 유포하거나 링크시키는 경우 <br />
 &ensp; 3. 불법복제 또는 해킹을 조장하는 내용인 경우 <br />
 &ensp; 4. 영리를 목적으로 하는 광고일 경우 <br />
 &ensp; 5. 범죄와 결부된다고 객관적으로 인정되는 내용일 경우 <br />
 &ensp; 6. 다른 이용자 또는 제 3자의 저작권 등 기타 권리를 침해하는 내용인 경우 <br />
 &ensp; 7. 사적인 정치적 판단이나 종교적 견해의 내용으로 회사가 서비스 성격에 부합하지 않는다고 판단하는 경우 <br />
 &ensp; 8. 회사에서 규정한 게시물 원칙에 어긋나거나, 게시판 성격에 부합하지 않는 경우 <br />
 &ensp; 9. 기타 관계법령에 위배된다고 판단되는 경우 <br />
 &ensp; 10. 회사는 게시물 등에 대하여 제3자로부터 명예훼손, 지적재산권 등의 권리 침해를 이유로 게시중단 요청을 받은 경우 이를 임시로 게시중단(전송중단)할 수 있으며, 게시중단 요청자와 게시물 등록자 간에 소송, 합의 기타 이에 준하는 관련기관의 결정 등이 이루어져 회사에 접수된 경우 이에 따릅니다. <br />
 &ensp; 11. 해당 게시물 등에 대해 임시게시 중단이 된 경우, 게시물을 등록한 회원은 재게시(전송재개)를 회사에 요청할 수 있으며, 게시 중단일로부터 3개월 내에 재 게시를 요청하지 아니한 경우 회사는 이를 삭제할 수 있습니다.</p>
<p><strong>제 18조 (게시물에 대한 저작권)</strong></p>
<p>① 회사가 작성한 게시물 또는 저작물에 대한 저작권 기타 지적재산권은 회사에 귀속합니다.<br />
  ② 회원이 서비스 내에 게시한 게시물의 저작권은 게시한 회원에게 귀속됩니다. 단, 회사는 서비스의 운영, 전시, 전송, 배포, 홍보의 목적으로 회원의 별도의 허락 없이 무상으로 저작권법에 규정하는 공정한 관행에 합치되게 합리적인 범위 내에서 다음과 같이 회원이 등록한 게시물을 사용할 수 있습니다.<br />
&ensp;  1. 서비스 내에서 회원 게시물의 복제, 수정, 개조, 전시, 전송, 배포 및 저작물성을 해치지 않는 범위 내에서의 편집 저작물 작성<br />
&ensp;  2. 미디어, 통신사 등 서비스 제휴 파트너에게 회원의 게시물 내용을 제공, 전시 혹은 홍보하게 하는 것. 단, 이 경우 회사는 별도의 동의 없이 회원의 이용자ID 외에 회원의 개인정보를 제공하지 않습니다.<br />
  </p>
<p><strong>제 19조 (서비스 이용시간)</strong></p>
<p>
① 서비스의 이용은 회사의 업무상 또는 기술상 특별한 지장이 없는 한 연중무휴 1일 24시간을 원칙으로 합니다. 다만, 정기점검, 긴급조치 등 회사가 서비스 점검 및 조치를 필요로 하는 시간은 그러하지 않습니다. <br />
② 회사는 서비스를 일정범위로 분할하여 각 범위별로 이용가능 시간을 별도로 정할 수 있습니다. 이 경우 그 내용을 사전에 공지합니다. 
</p>
<p><strong>제 20조 (서비스 이용책임)</strong></p>
<p>회원은 회사에서 공식적으로 인정한 경우를 제외하고는 서비스를 이용하여 상품을 판매하는 영업활동을 할 수 없으며 특히 해킹, 광고를 통한 수익, 음란사이트를 통한 상업행위, 상용S/W 불법배포 등을 할 수 없습니다. 이를 어기고 발생한 영업활동의 결과 및 손실, 관계기관에 의한 구속 등 법적 조치 등에 관해서는 회사가 책임을 지지 않습니다. </p>
<p><strong>제 21조 (서비스 제공의 중지) </strong></p>
<p>① 회사는 다음 각 호에 해당하는 경우 서비스 제공을 중지할 수 있습니다. <br />
&ensp;  1. 서비스용 설비의 보수 등 공사로 인한 부득이한 경우 <br />
&ensp;  2. 기타 불가항력적 사유가 있는 경우 <br />
  ② 회사는 국가비상사태, 정전, 제반 설비의 장애 또는 이용량의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 때에는 서비스의 전부 또는 일부를 제한 하거나 중지할 수 있습니다. </p>
<p><strong>제 22조 (광고주와의 거래) </strong></p>
<p>회사는 본 서비스상에 게재되어 있거나 본 서비스를 통한 광고주의 판촉활동에 회원이 참여하거나 교신 또는 거래의 결과로서 발생하는 모든 손실 또는 손해에 대해 책임을 지지 않습니다. </p>
<p><strong>제 23조 (링 크) </strong></p>
<p>본 서비스 또는 제3자는 월드와이드웹 사이트 또는 자료에 대한 링크를 제공할 수 있습니다. 
회사는 그러한 사이트 및 자료에 대한 아무런 통제권이 없으므로, 회원은 회사가 그와 같은 외부 사이트나 자료를 이용하는 것에 대해 책임이 없으며, 그러한 사이트나 자료에 대한, 또는 그로부터 이용 가능한 내용, 광고, 제품이나 재료에 대해 회사가 아무런 보증도 하지 않고, 그에 대해 책임이 없음을 인정하고 이에 동의합니다. 또한, 회원은 그러한 사이트나 자료에 대한, 또는 그를 통하여 이용 가능한 내용, 상품 또는 서비스를 이용하거나 이를 신뢰함으로 인해, 또는 이와 관련하여 야기되거나 야기되었다고 주장되는 어떠한 손해나 손실에 대하여 회사는 직접적 또는 간접적으로 책임을 지지 않음을 인정하고 이에 동의합니다. 
<br /></p>
<p><h2>제5장 계약해지 및 이용제한</h2></p>
