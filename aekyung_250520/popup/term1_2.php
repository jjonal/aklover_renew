<h2>제 2장 서비스 이용계약</h2>
<p><strong>제 6조 (이용계약의 성립) </strong></p>
<p>① "위의 이용약관에 동의하십니까?" 라는 이용신청시의 물음에 회원이 "동의" 단추를 누르면 이 약관에 동의하는 것으로 간주됩니다. <br />
  ② 이용계약은 제7조에 규정한 회원의 이용신청에 대하여 회사가 승낙함으로써 성립합니다. </p>
<p><strong>제 7조 (이용신청) </strong></p>
<p>① 본 서비스를 이용하기 위해서는 회사 소정의 가입신청 양식에서 요구하는 모든 이용자 정보를 기록하여 신청합니다. <br />
  ② 가입신청 양식에 기재하는 모든 이용자 정보는 모두 실제 데이터인 것으로 간주됩니다. 실명이나 실제 정보를 입력하지 않은 사용자는 법적인 보호를 받을 수 없으며 서비스의 제한을 받을 수 있습니다. </p>
<p><strong>제 8조 (개인정보의 보호 및 사용)</strong></p>
<p>① 회사는 관계 법령이 정하는 바에 따라 회원의 개인정보를 보호하기 위해 노력합니다. 개인정보의 보호 및 사용에 대해서는 관련 법령 및 회사의 개인정보 보호정책이 적용됩니다. 단, 회사의 공식 사이트 이외의 링크된 사이트에서는 회사의 개인정보 보호정책이 적용되지 않습니다. 또한, 회원은 비밀번호 등이 타인에게 노출되지 않도록 철저히 관리해야 하며 회사는 회원의 귀책사유로 인해 노출된 정보에 대해서 책임을 지지 않습니다.</p> 
<p>② 회사는 다음과 같은 경우에 법이 허용하는 범위 내에서 회원의 개인정보를 제3자에게 제공할 수 있습니다. <br />
&ensp;1. 수사기관이나 기타 정부기관으로부터 정보제공을 요청 받은 경우 <br />
&ensp;2. 회원의 법령 또는 약관의 위반을 포함하여 부정행위 확인 등의 정보보호 업무를 위해 필요한 경우 <br />
&ensp;3. 기타 법률에 의해 요구되는 경우<br />
</p>
<p><strong>제 9조(회원정보 사용에 대한 동의) </strong></p>
<p>① 회사는 제7조에 따라 가입 신청 양식에 기재된 회원의 개인정보를 본 이용계약의 이행과 본 이용계약상의 서비스 제공을 위한 목적으로 이용합니다. <br />
  ② 회원 정보는 회사와 제휴한 업체에 제공될 수 있습니다. 단, 회사는 회원 정보 제공 이전에 제휴 업체, 제공 목적, 제공할 회원 정보의 내용 등을 사전에 공지하고 회원의 동의를 얻어야 합니다. <br />
  ③ 회원은 회원 정보 수정을 통해 언제든지 개인 정보에 대한 열람 및 수정을 할 수 있습니다. <br />
  ④ 회원이 가입 신청 양식에 회원정보를 기재하고, 회사에 본 약관에 따라 이용신청을 하는 것은 회사가 본 약관에 따라 이용신청서에 기재된 회원정보를 수집, 이용 및 제공하는 것에 동의하는 것으로 간주됩니다.  </p>
<p><strong>제 10조(이용 신청의 승낙과 제한)</strong></p>
<p>① 회사는 제5조, 제6조의 규정에 의한 이용신청에 대하여 업무 수행상 또는 기술상 지장이 없는 경우에 원칙적으로 접수순서에 따라 서비스 이용을 승낙합니다.<br />
② 회사는 아래사항에 해당하는 경우에 대해서 승낙을 보류할 수 있습니다.<br />
&ensp;  1. 본인의 진정한 정보를 제공하지 아니한 이용신청의 경우<br />
&ensp;  2. 법령 위반 또는 사회의 안녕과 질서, 미풍양속을 저해할 목적으로 신청한 경우 <br />
&ensp;  3. 부정한 용도로 본 서비스를 이용하고자 하는 경우 <br />
