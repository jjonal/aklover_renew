&ensp;  9. 개인정보보호위원회, 소비자보호단체 등 공신력 있는 기관으로부터 시정요구를 받는 행위 <br />
&ensp;  10. 선거관리위원회의 중지, 경고 또는 시정명령을 받는 선거법 위반 행위 <br />
&ensp;  11. 기타 관계법령에 위배되는 행위 <br />
  ② 회원은 이 약관에서 규정하는 사항과 서비스 이용안내 또는 주의사항을 준수 하여야 합니다. <br />
  ③ 회원은 내용별로 회사가 서비스 공지사항에 게시하거나 별도로 공지한 이용 제한사항을 준수하여야 합니다. <br />
