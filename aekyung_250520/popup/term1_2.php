  ⑤ 회원은 이와 같은 영리행위에 대하여 회사에 손해배상의무를 집니다. <br />
  ⑥ 회원은 서비스의 이용권한, 기타 이용 계약상 지위를 타인에게 양도, 증여할 수 없으며, 이를 담보로 제공할 수 없습니다. <br />
  ⑦ 회원은 회사의 사전승낙 없이는 서비스의 전부 또는 일부 내용 및 기능을 전용할 수 없습니다. <br />
