  ① 회원이 서비스 이용계약을 해지하고자 할 경우에는 본인이 온라인 또는 회사가 정한 별도의 이용방법을 통해 회사에 해지신청을 하여야 합니다 <br />
  ② 회사는 회원이 다음 각 호의 1에 해당하는 행위를 하였을 경우 사전 통지 없이 이용계약을 해지하거나 또는 서비스 이용을 중지할 수 있습니다. <br />
