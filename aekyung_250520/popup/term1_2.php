  ③ 회사는 서비스 이용신청이 다음 각 호에 해당하는 경우에는 그 신청에 대하여 승낙 제한사유가 해소될 때까지 승낙을 유보할 수 있습니다.<br />
&ensp;  1. 회사가 설비의 여유가 없는 경우<br />
&ensp;  2. 회사의 기술상 지장이 있는 경우<br />
&ensp;  3. 기타 회사의 귀책사유로 이용승낙이 곤란한 경우<br />
  ④ 회사는 이용신청고객이 관계 법령에서 규정하는 미성년자일 경우에 서비스별 안내에서 정하는 바에 따라 승낙을 보류할 수 있습니다.<br />
  ⑤ 회사는 회원 가입 절차 완료 이후 제2항 각 호에 따른 사유가 발견된 경우 이용 승낙을 철회할 수 있습니다.</p>
<p><strong>제 11조 (이용자ID 부여 및 변경 등)</strong></p>
<p>① 회사는 회원에 대하여 약관에 정하는 바에 따라 이용자 ID를 부여합니다.<br />
  ② 이용자ID는 원칙적으로 변경이 불가하며 부득이한 사유로 인하여 변경 하고자 하는 경우에는 해당 ID를 해지하고 재가입해야 합니다.<br />
  ③ 이용자ID는 다음 각 호에 해당하는 경우에는 회원의 요청 또는 회사의 직권으로 변경 또는 이용을 정지할 수 있습니다.<br />
